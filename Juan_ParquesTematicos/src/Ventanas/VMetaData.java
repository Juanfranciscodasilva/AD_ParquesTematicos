    private void cbTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTablaActionPerformed
        int index = cbTabla.getSelectedIndex()-1;
        MetaDataTable tabla = null;
        if(index > -1){
            tabla = this.metaData.getListaTablas().get(index);
        }
        if(tabla == null){
            LimpiarCampos();
        }else{
            this.tablaSelected = tabla;
            AutocompletarDatosTabla();
        }
    }//GEN-LAST:event_cbTablaActionPerformed

