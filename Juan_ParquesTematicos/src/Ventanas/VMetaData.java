    public void RellenarMetaDatos(){
        tNombre.setText(this.metaData.getNombreBD());
        tDriver.setText(this.metaData.getDriverBD());
        tUrl.setText(this.metaData.getUrlDB());
        tCliente.setText(this.metaData.getClienteDB());
    }
    
    public void LimpiarCampos(){
        tEsquema.setText("");
        tColumnas.setText("");
    }
    
     public void AutocompletarDatosTabla(){
        tEsquema.setText(this.tablaSelected.getEsquema());
        StringBuilder textoColumnas = new StringBuilder();
        if(!this.tablaSelected.getListaColumnas().isEmpty()){
            textoColumnas.append("Columnas de la tabla "+this.tablaSelected.getNombre()+":\n\n");
            for(MetaDataColumn columna : this.tablaSelected.getListaColumnas()){
                textoColumnas.append(columna.toString()+"\n");
            }
        }else{
            textoColumnas.append("Ha ocurrido un problema al cargar las columnas de la tabla o la tabla está vacía.");
        }
        tColumnas.setText(textoColumnas.toString());
    }
     
     public void RellenarComboTablas(){
        cbTabla.removeAllItems();
        if(!this.metaData.getListaTablas().isEmpty()){
             cbTabla.addItem("-----Elegir tabla-----");
            try{
                for(MetaDataTable tabla : this.metaData.getListaTablas()){
                    cbTabla.addItem(tabla.getNombre());
                }
            }catch(Exception e){
                System.out.println("ha ocurrido un error "+ e.getMessage());
            } 
        }
    }

